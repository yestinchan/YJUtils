package com.iyestin.java.yjutils;

import java.io.IOException;

public class FileWriterInitException extends IOException{

	/**
	 * 
	 */
	private static final long serialVersionUID = 4369543270132838807L;

	public FileWriterInitException() {
		super();
	}

	public FileWriterInitException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public FileWriterInitException(String arg0) {
		super(arg0);
	}

	public FileWriterInitException(Throwable arg0) {
		super(arg0);
	}
	
	

}
